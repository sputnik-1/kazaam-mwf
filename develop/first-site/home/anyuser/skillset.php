<div class="box2">
<span class="subheading">TMDA - Tagged Message Delivery Agent</span>

<p class="p1">TMDA is an open source software application designed to
significantly reduce the amount of spam (Internet junk-mail) you
receive.</p>

<p>The technical countermeasures used by TMDA to thwart spam
include:</p>

<ul>

<li><span class="hdr7">whitelists:</span> - accept mail from known,
trusted senders.</li>

<li><span class="hdr7">blacklists:</span> - refuse mail from undesired
senders.</li>

<li><span class="hdr7">challenge/response:</span> - allows unknown senders
which aren't on the whitelist or blacklist the chance to confirm that
their message is legitimate (non-spam).</li>

<li><span class="hdr7">tagged addresses:</span> - special-purpose e-mail
addresses such as time-dependent addresses, or addresses which only
accept certain kinds of communication. These increase the transparency
of TMDA for unknown senders by allowing them to safely circumvent the
challenge/response system.</li>

</ul>

<p>
This combination was chosen based on the following assumptions
about the current state of spam on the Internet:
</p>
 
<ul>

<li>You cannot keep your email address secret from spammers.</li> 

<li>Content-based filters can't distinguish spam from legitimate mail
with sufficient accuracy.</li> 

<li>spam will not cease until it becomes prohibitively expensive for
spammers to operate.</li>

</ul>

<p>To maintain economies of scale, bulk-mailing is generally:</p>

<ul>

<li>An impersonal process where the recipient is not
distinguished.</li>

<li>A one-way communication channel (from spammer to victim).</li>

</ul>

<p><a href="http://wiki.tmda.net/AboutTmda"
